const EARTH_RADIUS_MI = 3959;
const rad = (deg: number) => (deg * Math.PI) / 180;

/** Great-circle distance in statute miles; null when either code is unknown. */
export function airportDistanceMiles(a: string, b: string): number | null {
  const ca = AIRPORT_COORDS[a];
  const cb = AIRPORT_COORDS[b];
  if (!ca || !cb) return null;
  const dLat = rad(cb[0] - ca[0]);
  const dLon = rad(cb[1] - ca[1]);
  const h =
    Math.sin(dLat / 2) ** 2 + Math.cos(rad(ca[0])) * Math.cos(rad(cb[0])) * Math.sin(dLon / 2) ** 2;
  return 2 * EARTH_RADIUS_MI * Math.asin(Math.sqrt(h));
}

// "On the way" = within 1.5x the direct great-circle (+250 mi for short hops).
// Past that the routing is flying the wrong direction; a spoke pair whose only
// routing is an off-axis hub (CLE-PIT via ORD, 6.9x) deliberately gets nothing.
const DETOUR_FACTOR = 1.5;
const DETOUR_SLACK_MI = 250;

/** Max total path miles allowed for an itinerary between two airports. */
export function detourBoundMiles(directMiles: number): number {
  return Math.max(directMiles * DETOUR_FACTOR, directMiles + DETOUR_SLACK_MI);
}
